        from ..utils.db import get_user_profile
        try:
            profile = get_user_profile()
            skill_level = profile.get("level", "Beginner")
        except Exception:
            skill_level = "Beginner"
            
        if skill_level not in ["Beginner", "Intermediate", "Advanced"]:
            skill_level = "Beginner"
            
        # Check if Gemini API key is available
        api_key = os.environ.get("GEMINI_API_KEY")
        if api_key:
            try:
                from google import genai
                from google.genai import types
                client = genai.Client(api_key=api_key)
                
                if notes_content and len(notes_content.strip()) > 50:
                    prompt = (
                        f"Generate a {num_questions}-question practice quiz based on the following study notes.\n\n"
                        f"Target Skill Level: {skill_level}\n\n"
                        f"Study Notes:\n{notes_content}\n\n"
                        f"Requirements:\n"
                        f"1. Generate exactly {num_questions} questions.\n"
                        f"2. Mix question types: include Multiple-Choice (type='mcq'), Short-Answer (type='short_answer'), and Coding (type='coding') questions.\n"
                        f"3. For MCQ questions, 'options' must have exactly 4 choices, and 'correct' must be the index of the correct option as a string ('0', '1', '2', or '3').\n"
                        f"4. For Short-Answer questions, 'options' must be empty, and 'correct' must be the exact answer term or short phrase.\n"
                        f"5. For Coding questions, 'options' must be empty, and 'correct' must be the correct programming code snippet solution.\n"
                        f"6. Provide a clear, educational explanation for the correct answer.\n"
                        f"7. Match the targeted skill level ({skill_level}) in difficulty.\n"
                        f"8. Respond ONLY with valid JSON matching the requested schema."
                    )
                else:
                    prompt = (
                        f"Generate a {num_questions}-question practice quiz on the topic: '{topic}'.\n\n"
                        f"Target Skill Level: {skill_level}\n\n"
                        f"Requirements:\n"
                        f"1. Generate exactly {num_questions} questions.\n"
                        f"2. Mix question types: include Multiple-Choice (type='mcq'), Short-Answer (type='short_answer'), and Coding (type='coding') questions.\n"
                        f"3. For MCQ questions, 'options' must have exactly 4 choices, and 'correct' must be the index of the correct option as a string ('0', '1', '2', or '3').\n"
                        f"4. For Short-Answer questions, 'options' must be empty, and 'correct' must be the exact answer term or short phrase.\n"
                        f"5. For Coding questions, 'options' must be empty, and 'correct' must be the correct programming code snippet solution.\n"
                        f"6. Provide a clear, educational explanation for the correct answer.\n"
                        f"7. Match the targeted skill level ({skill_level}) in difficulty.\n"
                        f"8. Respond ONLY with valid JSON matching the requested schema."
                    )
                
                response = client.models.generate_content(
                    model="gemini-2.5-flash",
                    contents=prompt,
                    config=types.GenerateContentConfig(
                        response_mime_type="application/json",
                        response_schema=QuizSchema,
                    ),
                )
                
                if response and response.text:
                    import json
                    data = json.loads(response.text)
                    questions = data.get("questions", [])
                    valid_questions = []
                    for q in questions:
                        if isinstance(q, dict) and "question" in q and "correct" in q and "explanation" in q:
                            q_type = q.get("type", "mcq")
                            correct_val = str(q["correct"]).strip()
                            if q_type == "mcq":
                                options_list = q.get("options", [])
                                if len(options_list) == 4 and correct_val.isdigit() and 0 <= int(correct_val) <= 3:
                                    q["correct"] = correct_val
                                    q["type"] = "mcq"
                                    valid_questions.append(q)
                            elif q_type in ["short_answer", "coding"]:
                                q["options"] = []
                                q["correct"] = correct_val
                                q["type"] = q_type
                                valid_questions.append(q)
                    if valid_questions:
                        return valid_questions[:num_questions]
            except Exception as e:
                print(f"Gemini API Quiz Exception: {str(e)}. Falling back to offline generator.")

        # Scenario 1: Generate from uploaded notes (offline rule-based)
        if notes_content and len(notes_content.strip()) > 50:
            return self._generate_from_notes(notes_content, num_questions)
            
        # Scenario 2: Pull from pre-built question bank (offline fallback)
        for key in self.question_bank:
            if key in topic or topic in key:
                # Retrieve questions for the specific level, fallback to Beginner if not present
                level_map = self.question_bank[key]
                questions = level_map.get(skill_level, level_map["Beginner"])
                
                shuffled = list(questions)
