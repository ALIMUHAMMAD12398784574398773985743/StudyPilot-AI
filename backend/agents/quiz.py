    def generate_quiz(self, topic: str, num_questions: int = 5, notes_content: str = None) -> List[Dict[str, Any]]:
        """
        Generates a quiz on a topic. If notes_content is provided, it extracts 
        questions dynamically. Uses Gemini when API key is active.
        """
        topic = sanitize_input_text(topic).lower()
        num_questions = max(1, min(10, num_questions))
        
