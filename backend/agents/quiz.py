        num_q = session_state.get("quiz_count", 5)
        notes = session_state.get("notes_text", None)
        
        quiz = self.generate_quiz(topic, num_q, notes)
        
        chat_format = [f"### 📝 Practice Quiz: {topic.capitalize()}"]
        for idx, q in enumerate(quiz):
            chat_format.append(f"\n**Q{idx+1}: {q['question']}**")
            for o_idx, opt in enumerate(q['options']):
                chat_format.append(f"  {chr(65+o_idx)}) {opt}")
        
        return "\n".join(chat_format)
